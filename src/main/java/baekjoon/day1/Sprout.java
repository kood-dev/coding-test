package baekjoon.day1;

/*
문자열 내 "를 넣고자 할떄 역슬래쉬 `\` 를 사용해야 함을 이해하고 있는가
문자열 개행 시에 대한 \n 에 대해 이해하고 있는가
 */
public class Sprout {
    public static void main(String[] args) {
        String print = "         ,r'\"7 \n" +
                "r`-_   ,'  ,/ \n" +
                " \\. \". L_r' \n" +
                "   `~\\/ \n" +
                "      | \n" +
                "      |";

        System.out.println(print);
    }
}
